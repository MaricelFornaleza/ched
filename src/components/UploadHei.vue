<template>
<div>
  <div
    v-if="!completed"
    class="container w-fit mx-auto flex flex-col items-center justify-center"
  >
    <div
        class="
          border-b-2
          text-center
          flex flex-col
          justify-center
          items-center
          space-y-3
          w-full
          pb-3
          border-dark-100
          mb-4
        "
      >
        <div class="rounded-full bg-warning-light h-fit w-fit p-4 text-warning">
          <LibraryIcon class="h-10" />
        </div>
        <p class="uppercase font-bold label-sm">Higher Education Institution</p>
      </div>

    <div v-if="dropzoneFile === ''" class="mt-10 w-full">
      <DropZone @drop.prevent="drop" @change="selectedFile" />
      <!-- <span class="text-xs font-bold">File: {{ dropzoneFile.name }}</span> -->
    </div>

    <div
      v-else
      class="my-20 w-fit flex justify-between p-5 border border-light-300"
    >
      <div class="flex space-x-5">
        <img
          src="@/assets/img/xls.png"
          class="h-8"
          alt="XLS Icon by Dimitry Miroliubov"
        />
        <div class="text-base">{{ dropzoneFile.name }}</div>
      </div>
    </div>

    <div class="flex items-center justify-center space-x-5 mt-5">
      <button class="btn-sm bg-light-300 font-bold" type="button">
          Cancel
        </button>
        <button
          v-if="!completed"
          class="btn-sm bg-brand-blue text-light-100 font-bold"
          type="submit"
          @click="upload()"
        >
          Upload
        </button>
    </div>
    </div>
<div
    v-else
    class="container w-fit mx-auto flex flex-col items-center justify-center"
  >
  </div>
    <ModalWidget v-show="visible">
      <template #body>
        <div
          class="
            inline-flex
            items-center
            px-4
            py-4
            font-semibold
            leading-6
            text-sm
            shadow
            rounded-md
            text-light-100
            bg-brand-blue
            hover:bg-brand-blue
            transition
            ease-in-out
            duration-150
            cursor-progress
          "
          disabled
        >
          <svg
            class="animate-spin -ml-1 mr-3 h-5 w-5 text-light-100"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
          >  
            <circle
              class="opacity-25"
              cx="12"
              cy="12"
              r="10"
              stroke="currentColor"
              stroke-width="4"
            ></circle>
            <path
              class="opacity-75"
              fill="currentColor"
              d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
            ></path>
          </svg>
          Processing...
        </div> 
      </template>
    </ModalWidget>   
  </div>
</template>

<script>
import DropZone from "@/partials/DropZone.vue";
//import AlertWidget from "@/partials/AlertWidget.vue";
import ModalWidget from "@/partials/ModalWidget.vue";
import { ref } from "vue";
import Worker from "@/assets/js/parseHei.worker.js";
import { LibraryIcon } from "@heroicons/vue/solid";
export default {
  data() {
    return {
      visible: false,
      completed: false,
      className: "alert-info",
      table_headers: { A: "NO.", B: "NAME" },
      heis: [],
      excelData: [],
      worker: undefined,
    };
  },
  components: {
    LibraryIcon,
   // AlertWidget,
    DropZone,
    ModalWidget,
  },
  setup() {
    let dropzoneFile = ref("");
    const drop = (e) => {
      dropzoneFile.value = e.dataTransfer.files[0];
    }; 
    const selectedFile = () => {
      dropzoneFile.value = document.querySelector(".dropzoneFile").files[0];
    };    
    
    return { dropzoneFile, drop, selectedFile };
  },
  methods: {
    upload() {
      var regex = /^([a-zA-Z0-9\s_\\.\-:])+(.xls|.xlsx)$/;

      if (this.dropzoneFile === "") {
        this.className = "alert-error";
      } else if (regex.test(this.dropzoneFile.name)) {
        // alert(`Submitted Files:\n${this.dropzoneFile.name}`);
        this.visible = true;
        let _this = this;
        var reader = new FileReader();
        reader.onload = function (e) {
          var data = e.target.result;

          if (typeof Worker !== "undefined") {
            if (typeof _this.worker == "undefined") {
              _this.worker = new Worker();
            }
            _this.worker.postMessage({
              d: data,
            });

            //can be improved by abstraction
            _this.worker.onmessage = function (event) {
              _this.table_headers = event.data.headers;
              _this.heis = event.data.rows;
              console.log(_this.heis);

              if (event.data.complete) {
                _this.visible = false;
               //_this.$emit("complete", step);
                _this.completed = !_this.completed;
              }
            };
          }
        };
        reader.readAsArrayBuffer(this.dropzoneFile);
      } else {
        alert("Please upload a .xlsx file!");
      }
    },
  },
};
</script>
